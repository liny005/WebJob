-- 5. 操作审计日志表 (JOB_AUDIT_LOG)
-- ============================================
CREATE TABLE IF NOT EXISTS `JOB_AUDIT_LOG` (
    `ID` BIGINT NOT NULL AUTO_INCREMENT COMMENT '日志ID',
    `OPERATOR` VARCHAR(50) NOT NULL COMMENT '操作人用户名',
    `OPERATOR_DISPLAY_NAME` VARCHAR(100) NULL COMMENT '操作人显示名称',
    `ACTION` VARCHAR(100) NOT NULL COMMENT '操作功能',
    `TARGET` VARCHAR(200) NULL COMMENT '操作对象',
    `REMARK` TEXT NULL COMMENT '备注（可存储JSON等较长内容）',
    `CREATED_AT` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '操作时间',
    PRIMARY KEY (`ID`),
    INDEX `IX_AUDIT_LOG_OPERATOR` (`OPERATOR`),
    INDEX `IX_AUDIT_LOG_CREATED_AT` (`CREATED_AT`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='用户操作审计日志表';

-- ============================================
-- 4. 推送配置表 (JOB_NOTIFY_CONFIG_JSON)  ← 实体类 NotifyConfigEntity 对应此表
-- ============================================
CREATE TABLE IF NOT EXISTS `JOB_NOTIFY_CONFIG_JSON` (
    `ID` BIGINT NOT NULL AUTO_INCREMENT COMMENT '配置ID',
    `NAME` VARCHAR(100) NOT NULL COMMENT '配置名称',
    `CHANNEL` VARCHAR(50) NOT NULL DEFAULT 'DingTalk' COMMENT '渠道类型: DingTalk/Feishu/Email',
    `CONFIG` TEXT NOT NULL COMMENT '渠道配置 JSON，钉钉: {"webhookUrl":"...","secret":"..."}',
    `IS_ENABLED` TINYINT(1) NOT NULL DEFAULT 1 COMMENT '是否启用',
    `CREATED_AT` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATED_AT` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='推送配置表';

-- ============================================
-- 6. 任务扩展配置表 (JOB_CONFIG)  — 替代 Quartz JobDataMap
-- ============================================
CREATE TABLE IF NOT EXISTS `JOB_CONFIG` (
    `ID` BIGINT NOT NULL AUTO_INCREMENT COMMENT '配置ID',
    `JOB_NAME` VARCHAR(200) NOT NULL COMMENT '任务名称（对应 Quartz JobKey.Name）',
    `JOB_GROUP` VARCHAR(200) NOT NULL COMMENT '任务分组（对应 Quartz JobKey.Group）',
    `DESCRIPTION` VARCHAR(500) NULL COMMENT '任务描述',
    `REQUEST_URL` VARCHAR(1000) NOT NULL DEFAULT '' COMMENT '请求 URL',
    `REQUEST_TYPE` INT NOT NULL DEFAULT 2 COMMENT '请求类型（1=GET 2=POST 3=PUT 4=DELETE）',
    `HEADERS` TEXT NULL COMMENT '请求头 JSON',
    `REQUEST_PARAMETERS` TEXT NULL COMMENT '请求参数 JSON',
    `TRIGGER_TYPE` INT NOT NULL DEFAULT 2 COMMENT '触发器类型（1=Cron 2=间隔秒）',
    `CRON` VARCHAR(100) NULL COMMENT 'Cron 表达式',
    `INTERVAL_SECOND` INT NULL COMMENT '间隔秒数',
    `BEGIN_TIME` DATETIME NULL COMMENT '开始时间',
    `END_TIME` DATETIME NULL COMMENT '结束时间（null 表示永不结束）',
    `RUN_TOTAL` INT NULL COMMENT '执行次数上限（0 或 null 表示无限制）',
    `DINGTALK` INT NOT NULL DEFAULT 0 COMMENT '钉钉通知级别（0=不通知 1=仅失败 2=全部）',
    `MAIL_MESSAGE` INT NOT NULL DEFAULT 0 COMMENT '邮件通知级别（0=不通知 1=仅失败 2=全部）',
    `CREATED_AT` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATED_AT` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`ID`),
    UNIQUE INDEX `IX_JOB_CONFIG_NAME_GROUP` (`JOB_NAME`, `JOB_GROUP`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='任务扩展配置表（替代 Quartz JobDataMap）';

