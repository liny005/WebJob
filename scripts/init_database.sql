-- DotJob 数据库初始化脚本
-- 适用于 MySQL 数据库
-- ============================================
-- 1. 用户表 (JOB_USER)
-- ============================================
CREATE TABLE IF NOT EXISTS `JOB_USER` (
    `ID` BIGINT NOT NULL AUTO_INCREMENT COMMENT '用户ID',
    `USERNAME` VARCHAR(50) NOT NULL COMMENT '用户名',
    `PASSWORD` VARCHAR(256) NOT NULL COMMENT '密码(已加密)',
    `DISPLAY_NAME` VARCHAR(100) NULL COMMENT '显示名称',
    `EMAIL` VARCHAR(100) NULL COMMENT '邮箱',
    `IS_ENABLED` TINYINT(1) NOT NULL DEFAULT 1 COMMENT '是否启用',
    `CREATED_AT` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `LAST_LOGIN_AT` DATETIME NULL COMMENT '最后登录时间',
    `ROLE` VARCHAR(20) NOT NULL DEFAULT 'User' COMMENT '角色: Admin, User',
    PRIMARY KEY (`ID`),
    UNIQUE INDEX `IX_JOB_USER_USERNAME` (`USERNAME`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='用户表';

-- ============================================
-- 2. 日志表 (JOB_LOG)
-- ============================================
CREATE TABLE IF NOT EXISTS `JOB_LOG` (
    `ID` BIGINT NOT NULL AUTO_INCREMENT COMMENT '日志ID',
    `BEGIN_TIME` DATETIME NOT NULL COMMENT '开始时间',
    `END_TIME` DATETIME NOT NULL COMMENT '结束时间',
    `JOB_NAME` VARCHAR(200) NULL COMMENT '任务名称(格式: Group.Name)',
    `JOB_GROUP` VARCHAR(100) NULL COMMENT '任务分组',
    `ERROR_MSG` TEXT NULL COMMENT '错误信息',
    `EXECUTE_TIME` DOUBLE NOT NULL DEFAULT 0 COMMENT '执行耗时(秒)',
    `PARAMETERS` TEXT NULL COMMENT '请求参数',
    `URL` VARCHAR(500) NULL COMMENT '请求URL',
    `REQUEST_TYPE` VARCHAR(20) NULL COMMENT '请求类型',
    `RESULT` TEXT NULL COMMENT '返回结果',
    `STATUS_CODE` INT NULL COMMENT 'HTTP状态码',
    `EXECUTION_STATUS` INT NOT NULL DEFAULT 0 COMMENT '执行状态: 0-进行中, 1-成功, 2-失败',
    `CREATED_AT` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    PRIMARY KEY (`ID`),
    INDEX `IX_JOB_LOG_JOB_NAME` (`JOB_NAME`),
    INDEX `IX_JOB_LOG_BEGIN_TIME` (`BEGIN_TIME`),
    INDEX `IX_JOB_LOG_EXECUTION_STATUS` (`EXECUTION_STATUS`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='任务执行日志表';

-- ============================================
-- 3. Quartz.NET 相关表 (如果不存在)
-- ============================================
-- 注意: 以下是 Quartz.NET 所需的表结构
